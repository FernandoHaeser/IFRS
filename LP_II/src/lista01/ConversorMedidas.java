/*

9. Conversor de Medidas (Com Métodos)

Crie um menu para converter metros em Centímetros ou Quilômetros.
Implemente um método para cada conversão e chame-os dentro do método main.

Exemplo: Se o usuário digitar 1 metro e escolher centímetros, a saída é "100.0 cm".

*/

package lista01;

import java.util.Scanner;

public class ConversorMedidas {

    // 1m -> 100cm
    // 1km -> 1000m

    public static double metersToCentimeters(double meters) {
        return meters * 100;
    }

    public static double metersToKilometers(double meters) {
        return meters / 1000;
    }

    public static void main(String[] args) {

        Scanner sc = new Scanner(System.in);

        String options = """
                [1] - Centímetros
                [2] - Quilômetros
                """;
        double result;

        System.out.print("Informe um valor em metros para ser convertido: ");
        double meters = sc.nextDouble();

        System.out.print("\nVocê deseja converter para qual medida?\n" +
                options +
                "\nR: ");
        int answer = sc.nextInt();

        switch (answer) {
            case 1:
                result = metersToCentimeters(meters);
                System.out.println("\nResultado da conversão: " + result + "cm");
                break;
            case 2:
                result = metersToKilometers(meters);
                System.out.println("\nResultado da conversão: " + result + "km");
                break;
            default:
                System.out.println("\nVocê não definiu nenhuma opção válida!");
                System.exit(0);
        }
        sc.close();
    }
}

